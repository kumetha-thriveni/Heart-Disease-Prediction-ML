import streamlit as st
import pandas as pd
import numpy as np
import pickle
from sklearn.ensemble import RandomForestClassifier

# Title
st.title("Heart Disease Prediction Dashboard")

st.write("Enter patient details to predict heart disease risk")

# Load dataset
df = pd.read_csv("heart.csv")

# Features and target
X = df.drop("target", axis=1)
y = df["target"]

# Train model
model = RandomForestClassifier()
model.fit(X, y)

# Sidebar inputs
st.sidebar.header("Patient Input Features")

def user_input():
    age = st.sidebar.slider("Age", 20, 80, 50)
    sex = st.sidebar.selectbox("Sex", [0,1])
    cp = st.sidebar.selectbox("Chest Pain Type", [0,1,2,3])
    trestbps = st.sidebar.slider("Resting Blood Pressure", 90, 200, 120)
    chol = st.sidebar.slider("Cholesterol", 100, 400, 200)
    fbs = st.sidebar.selectbox("Fasting Blood Sugar", [0,1])
    restecg = st.sidebar.selectbox("Rest ECG", [0,1,2])
    thalach = st.sidebar.slider("Max Heart Rate", 70, 210, 150)
    exang = st.sidebar.selectbox("Exercise Angina", [0,1])
    oldpeak = st.sidebar.slider("Oldpeak", 0.0, 6.0, 1.0)
    slope = st.sidebar.selectbox("Slope", [0,1,2])
    ca = st.sidebar.selectbox("Major Vessels", [0,1,2,3])
    thal = st.sidebar.selectbox("Thal", [0,1,2,3])

    data = {
        'age': age,
        'sex': sex,
        'cp': cp,
        'trestbps': trestbps,
        'chol': chol,
        'fbs': fbs,
        'restecg': restecg,
        'thalach': thalach,
        'exang': exang,
        'oldpeak': oldpeak,
        'slope': slope,
        'ca': ca,
        'thal': thal
    }

    features = pd.DataFrame(data, index=[0])
    return features

input_df = user_input()

st.subheader("Patient Input")
st.write(input_df)

# Prediction
prediction = model.predict(input_df)
prediction_proba = model.predict_proba(input_df)

st.subheader("Prediction Result")

if prediction[0] == 1:
    st.error("⚠ High Risk of Heart Disease")
else:
    st.success("✅ Low Risk of Heart Disease")

st.subheader("Prediction Probability")

st.write(prediction_proba)

# Show dataset
if st.checkbox("Show Dataset"):
    st.write(df)
